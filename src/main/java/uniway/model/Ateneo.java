package uniway.model;

public class Ateneo {
    private String nome;
    private String regione;
    private String provincia;
    private String sito;

    public Ateneo(String nome, String regione, String provincia, String sito) {
        this.nome = nome;
        this.regione = regione;
        this.provincia = provincia;
        this.sito = sito;
    }
}
